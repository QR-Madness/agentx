// Generates src/generated/markdown-manifest.ts — the token estimate for every Markdown
// twin the site serves, keyed by the twin's URL path.
//
// Why a generated module rather than a runtime read: `middleware.ts` runs on Vercel's
// edge, where there is no filesystem. The estimate has to be baked in at bundle time.
//
// Why committed rather than gitignored: Vercel bundles `middleware.ts` as part of the
// build, and the platform does not document whether that happens before or after the
// framework's build command runs. Committing the output removes the ordering question —
// a valid manifest is always on disk. `prebuild` refreshes it so it can't go stale
// through a normal build/deploy.
//
// Drift is non-fatal by design: the middleware negotiates by rule and only *looks up*
// the token count here, so a stale entry costs the `x-markdown-tokens` header, never a
// wrong page. `scripts/check_docs_site.py` warns when it drifts.
//
// Run: node scripts/gen-markdown-manifest.mjs   (wired into `bun run build` as prebuild)
import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const SITE = fileURLToPath(new URL('..', import.meta.url));
const DOCS = join(SITE, 'src/content/docs');
// NB: not src/data/ — the repo's .gitignore has a bare `data/` (the Docker bind-mount),
// which matches a directory of that name at any depth. A twin parked there builds fine
// locally and is silently never committed; the deploy is the first thing to notice.
const HOMEPAGE = join(SITE, 'src/content/homepage.md');
const OUT = join(SITE, 'src/generated/markdown-manifest.ts');

/** Astro's `entry.body` excludes frontmatter, so the estimate must too. */
function stripFrontmatter(source) {
  return source.startsWith('---') ? source.replace(/^---\r?\n[\s\S]*?\r?\n---\r?\n?/, '') : source;
}

/**
 * Rough token estimate: ~4 characters per token.
 *
 * Deliberately dependency-free. A real BPE tokenizer (gpt-tokenizer) unpacks to ~53MB
 * for a number that is an estimate no matter what — every model tokenizes differently,
 * and Cloudflare's equivalent header is documented as an estimate too. Agents use this
 * to decide "can I afford to fetch this", where order-of-magnitude is the whole point.
 */
function estimateTokens(text) {
  return Math.ceil(text.length / 4);
}

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full));
    else if (entry.name.endsWith('.md')) out.push(full);
  }
  return out;
}

const manifest = {};

// The homepage twin, served by src/pages/index.md.ts.
if (!existsSync(HOMEPAGE)) {
  console.error(`gen-markdown-manifest: no homepage twin at ${relative(SITE, HOMEPAGE)}.`);
  console.error('If it exists on disk, check `git check-ignore` — see the note above.');
  process.exit(1);
}
manifest['/index.md'] = estimateTokens(stripFrontmatter(readFileSync(HOMEPAGE, 'utf8')));

// The docs twins, served by src/pages/docs/[...slug].md.ts. The glob loader's `id` is
// the file path minus extension, which is exactly the twin's URL path under /docs.
for (const file of walk(DOCS)) {
  const id = relative(DOCS, file).replace(/\\/g, '/').replace(/\.md$/, '');
  manifest[`/docs/${id}.md`] = estimateTokens(stripFrontmatter(readFileSync(file, 'utf8')));
}

const entries = Object.keys(manifest)
  .sort()
  .map((path) => `  '${path}': ${manifest[path]},`)
  .join('\n');

const banner = `// GENERATED by scripts/gen-markdown-manifest.mjs — do not edit by hand.
// Regenerate with: node scripts/gen-markdown-manifest.mjs (runs automatically on \`bun run build\`).
//
// Estimated tokens for each Markdown twin, keyed by URL path. Consumed by middleware.ts
// to set \`x-markdown-tokens\` when it serves a twin under Accept negotiation.

export const MARKDOWN_TOKENS: Record<string, number> = {
${entries}
};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, banner);
console.log(`markdown-manifest: ${Object.keys(manifest).length} twins → ${relative(SITE, OUT)}`);
